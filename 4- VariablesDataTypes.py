#tuple - A tuple is an ordered collection of data with elements separated by a comma and enclosed within parentheses. Tuples are immutable and cannot be modified after creation.
tuple1 = (('parrot', 'sparrow'), ('Priyanshu','Pandey'))
print(tuple1)

#Mapped Data - Dict: A dictionary is an unordered collection of data containing key:value pairs. The key:value pairs are enclosed within curly brackets.
dict1 = {"name":"sakshi", "age":20, "canVote":True}
print(dict1)

