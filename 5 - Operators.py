#operators in Python
#Addition - 15+7, +, for adding values
print(5+6)
#Subtraction - 15-7, -, for subtraction of values
print(15-6)
#Multiplication - 5*7, *, for multiplication of values
print(5*7)
#Exponential - 5**3, **, for repetitive multiplication, square, cube, power operator
print(5**3)
#Division - 5/3, /, for division, simple division
print(15/7)
#Modulus - 15%7, %, gives the remainder, when dividing
print(15%7)
#Floor Division - 15//7, //, gives only the quotient
print(15//7)